package valueibject;

public class VCLecture {

	private String id;
	private String GN;
	private String N;
	private String Grade;
	private String time;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getGN() {
		return GN;
	}

	public void setGN(String GN) {
		this.GN = GN;
	}

	public String getN() {
		return N;
	}

	public void setN(String N) {
		this.N = N;
	}

	public String getGrade() {
		return Grade;
	}

	public void setGrade(String Grade) {
		this.Grade = Grade;
	}

	public String gettime() {
		return time;
	}

	public void settime(String time) {
		this.time = time;
	}

}
